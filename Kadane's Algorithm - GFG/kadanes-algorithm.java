class Solution{

    // arr: input array
    // n: size of array
    //Function to find the sum of contiguous subarray with maximum sum.
    long maxSubarraySum(int arr[], int n){
        
        // Your code here
        int max = Integer.MIN_VALUE;
        int  ans = 0;
        
        for(int i=0; i<n; i++){
            ans+= arr[i];
            if(ans<0){
                max = Math.max(ans, max);
                ans = 0;
            }
            else max = Math.max(ans, max);
        }
        return max;
        
    }
    
}

